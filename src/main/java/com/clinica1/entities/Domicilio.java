package com.clinica1.entities;

import org.hibernate.annotations.Generated;

import javax.persistence.*;
import java.time.LocalDateTime;
@Entity
@Table(name = "domicilio")
public class Domicilio {

    @Id
    @SequenceGenerator(name = "domicilio_sequance", sequenceName = "domicilio_sequance",allocationSize = 1)
    @GeneratedValue(strategy = GenerationType.SEQUENCE, generator="sequence_generator")
    private Long id;
    private String calle;
    private String numero;
    private String localidad;
    private String provincia;
    @OneToOne(mappedBy = "domicilio")
    private Paciente paciente;

    public Domicilio(Long id, String calle, String numero, String localidad, String provincia) {
        this.id = id;
        this.calle = calle;
        this.numero = numero;
        this.localidad = localidad;
        this.provincia = provincia;
    }

    public Domicilio(String calle, String numero, String localidad, String provincia) {
        this.calle = calle;
        this.numero = numero;
        this.localidad = localidad;
        this.provincia = provincia;
    }

    public Domicilio() {    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getCalle() {
        return calle;
    }

    public void setCalle(String calle) {
        this.calle = calle;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getLocalidad() {
        return localidad;
    }

    public void setLocalidad(String localidad) {
        this.localidad = localidad;
    }

    public String getProvincia() {
        return provincia;
    }

    public void setProvincia(String provincia) {
        this.provincia = provincia;
    }

    public Paciente getPaciente() {
        return paciente;
    }

    public void setPaciente(Paciente paciente) {
        this.paciente = paciente;
    }

    @Override
    public String toString() {
        return "Domicilio{" +
                "id=" + id +
                ", calle='" + calle + '\'' +
                ", numero='" + numero + '\'' +
                ", localidad='" + localidad + '\'' +
                ", provincia='" + provincia + '\'' +
                ", paciente=" + paciente +
                '}';
    }
}
